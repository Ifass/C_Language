# include<stdio.h>
# include<math.h>

int main(){

// "==" equal to
    printf("%d \n" , 4 == 4);
    printf("%d \n" , 3 == 4);

// ">" , ">=" greater than & greater than equal to
    printf("%d \n" , 4 > 3 );
    printf("%d \n" , 3 > 4 );
    printf("%d \n" , 3 > 3 );
    printf("%d \n" , 3 >= 3 );
    printf("%d \n" , 3 >= 3 );
    printf("%d \n" , 4 >= 3 );


// "<" , "<=" smaller than & smaller than equal to
    printf("%d \n" , 2 < 3 );
    printf("%d \n" , 3 < 3 );
    printf("%d \n" , 4 < 3 );
    printf("%d \n" , 4 <= 3 );
    printf("%d \n" , 4 <= 4 );
    printf("%d \n" , 4 <= 5 );

// "!=" not equal to
    printf("%d \n" , 4 != 5 );
    printf("%d \n" , 5 != 5 );


    return 0;
}